/* ARITHMATIC OPERATORS */
/* 
+ addition
- substraction
% modulous (prints the reminder)
* multiplication
/ division
** exponential
++ increment
-- decrement

a++ ( first print then increment)
++a ( first increment then print)

a-- ( first print then decrement )
--a ( first decrement then print )

*/
let a = 10
console.log(a++) // first it will print the initial value  ( output: 10 )
console.log(a) // then it will print the incremental value ( output: 11 )

console.log(++a)  // print 11

