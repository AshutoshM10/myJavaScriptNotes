// comparision operator
// === equal value & type
// !== not equal to value & not equal to type
const num1 = 5;
const num2 = "5";

console.log(num1 === num2);  // false, because num1 is a number and num2 is a string

const value1 = 10;
const value2 = "10";

console.log(value1 !== value2);  // true, because even though their values are the same, their types are different


const number = 7;
const string = "7";

console.log(number !== string);  // true, because their values are the same, but their types are different
